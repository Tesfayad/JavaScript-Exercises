// addition calculator function

function add(a,b) {
    return a + b;
}
let sum = add(5, 6);

// calculate subtraction function
function sub(a,b) {
    return a - b;
}
let subtract = sub(9, 3);

// calculate multiplication function
function multiply(a,b) {
    return a * b;
}
let multiplication = multiply(8, 6);

// calculate division function
function div(a,b) {
    return a / b;
}
let divide = div(12, 7);

console.log(`add: ${sum}
sub: ${subtract}
multiply: ${multiplication}
div: ${divide}`);

